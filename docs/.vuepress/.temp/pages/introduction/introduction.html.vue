<p><a href="https://github.com/jito-foundation/restaking" target="_blank" rel="noopener noreferrer">Jito (Re)staking</a> is a multi-asset staking protocol for node consensus networks. The protocol tokenizes staked assets as vault receipt tokens (VRT) for enhanced liquidity and composability. Node consensus networks can use Jito Restaking to easily customize staking parameters, and economic incentives to tailor their security and tokenomics.</p>
<h2 id="what-is-ncn" tabindex="-1"><a class="header-anchor" href="#what-is-ncn"><span>What is NCN</span></a></h2>
<p>A Node Consensus Network (NCN) is simply a distributed network that validates and confirms information.</p>
<ul>
<li>Node: A piece of software running the required protocols as specified by the node consensus network.</li>
<li>Node Consensus Network (NCN): A set of nodes running software for the same network, working together to achieve consensus and provide services.</li>
<li>Operator: An entity that manages one or more nodes in the node consensus network.</li>
</ul>
