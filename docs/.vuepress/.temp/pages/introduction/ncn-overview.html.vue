<h2 id="what-is-a-node-consensus-network-ncn" tabindex="-1"><a class="header-anchor" href="#what-is-a-node-consensus-network-ncn"><span>What is a Node Consensus Network (NCN)?</span></a></h2>
<p>A Node Consensus Network (NCN) on Jito Restaking is simply a distributed network that validates and confirms information.</p>
<ul>
<li>Node: A piece of software running the required protocols as specified by the node consensus network.</li>
<li>Node Consensus Network (NCN): A set of nodes running software for the same network, working together to achieve consensus and provide services.</li>
<li>Operator: An entity that manages one or more nodes in the node consensus network.</li>
</ul>
<h2 id="what-can-you-create-as-an-ncn" tabindex="-1"><a class="header-anchor" href="#what-can-you-create-as-an-ncn"><span>What Can You Create as an NCN?</span></a></h2>
<p>NCN design encompasses a wide range of possibilities, covering any off-chain service that can be validated on-chain. This flexibility empowers NCN developers to craft custom validation mechanisms tailored to the specific needs of their services. The sole requirement is that some form of proof for the off-chain service's execution must be submitted on-chain to facilitate its validation.</p>
<p>Examples of NCNs include:</p>
<ul>
<li>Blockchains</li>
<li>Cross-chain bridges</li>
<li>Co-processor networks</li>
<li>Interoperability solutions</li>
<li>Applications</li>
<li>Solver networks</li>
<li>Oracle networks</li>
<li>And more!</li>
</ul>
